#include <exec.h>

void	ex_pipe(t_executor *ex)
{
	ex->save_in = ex->in_fd;
	if (pipe(ex->pipe_fd) < 0)
	{
		exit(1);
	}
	ex->in_fd = ex->pipe_fd[0];
	ex->out_fd = ex->pipe_fd[1];
}
